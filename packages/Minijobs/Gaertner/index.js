mp.events.add("server:MiniJobGaertner:startJob", (player) => {
  if(!mp.players.exists(player)) return;
  if ((!player.getVariable("isGardenerMinijob"))) {
    if (gm.weather.currentWeather !== "RAIN" && gm.weather.currentWeather !== "THUNDER") {
      let pos = new mp.Vector3(-1329.12, 66.48, 53.53);
      if(gm.functions.getVehicleFromPosition(pos,3).length > 0) {
        player.notify("Es steht bereits ein Fahrzeug in der Ausfahrt!");
        return;
      }

      let rand = Math.random() * (5 - 1) + 1;
      rand = parseInt(rand);
      player.setVariable("isGardenerMinijob","true");
      player.setVariable("gardenerMiniJobRoute",String(rand));
      player.setVariable("gardenerMiniJobWaypoint","1");
      var veh = mp.vehicles.new(1783355638, pos, {});
      if (gm.vehicleData[""+1783355638]) {
        vehData = gm.vehicleData[""+1783355638];
        veh.setVariable("tankvolumen",String(vehData.tankvolumen));
        veh.setVariable("verbrauch",String(vehData.verbrauch));
        veh.setVariable("treibstoff",String(vehData.treibstoff));
        veh.setVariable("vehData",JSON.stringify(vehData));
      } else {
        vehData = gm.vehicleData["undefiniert"];
        veh.setVariable("tankvolumen",String(vehData.tankvolumen));
        veh.setVariable("verbrauch",String(vehData.verbrauch));
        veh.setVariable("treibstoff",String(vehData.treibstoff));
        veh.setVariable("vehData",JSON.stringify(vehData));
      }
      veh.locked = false;
      veh.dead = false;
      veh.engine = true;
      veh.setVariable("fuel",String(100.00));
      veh.setVariable("isRunning","true");
      veh.setVariable("canStart","true");
      veh.setVariable("misfueled","false");
      veh.setVariable("owner",String(player.data.internalId));
      veh.setVariable("driver",String(player.id));

      player.vehicle = veh;
      player.call("client:MiniJobGaertner:startJob", [rand,1]);
    } else {
      player.notify("Tut mir leid, bei Regen mähen wir nicht!");
    }
  } else {
    player.notify("Du hast bereits einen Minijob. Bitte beende diesen erstmal.");
  }
});

mp.events.add("server:MiniJobGaertner:endJob", (player) => {
  if(!mp.players.exists(player)) return;
  
  let pos = new mp.Vector3(-1329.12, 66.48, 53.53);
  let vehs = gm.functions.getVehicleFromPosition(pos, 2);
  let veh = vehs[0];

  if(vehs.length > 1) {
    player.notify("Es steht bereits ein Fahrzeug in der Ausfahrt!");
    return;
  } else if (vehs.length == 1) {
    if (String(veh.model) != "1783355638") {
      player.notify("Dies kannst du nur mit dem Rasenmäher!");
      return;
    } else {
      if (veh) veh.destroy();
      var route = player.getVariable("gardenerMiniJobRoute");
      player.setVariable("isGardenerMinijob","false");
      player.setVariable("gardenerMiniJobRoute","0");

      var payout = 0.00;
      if (route == "1") payout = 18.00;
      if (route == "2") payout = 18.90;
      if (route == "3") payout = 15.93;
      if (route == "4") payout = 23.40;
      if (route == "5") payout = 10.35;
      player.notify("Du hast "+payout+"$ verdient!");


      gm.databaseManager.getConnection().query("SELECT id FROM bank_konten WHERE ownerId = ?", [player.data.internalId], function (err, res) {
        if (err) console.log(err);

        if (res.length > 0) {
          gm.databaseManager.getConnection().query("SELECT amout FROM bank_konten WHERE ownerId = ?", [player.data.internalId], function (err2, res2) {
            if (err2) console.log(err2);

            res2.forEach(function (am) {
              var newAm = am.amout + payout;

              gm.databaseManager.getConnection().query("UPDATE bank_konten SET amout = ? WHERE ownerId = ?", [newAm, player.data.internalId], function (err3, res3) {
                  if (err3) console.log(err3);
              });
            });
          });
        } else {
          player.notify("Du besitzt kein Bankkonto!");
        }
      });
    }
  } else {
    player.notify("Es steht kein Rasenmäher im Einparkpunkt!")
  }
});
